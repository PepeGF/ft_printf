#include "ft_printf.h"

void	ft_putnbr(int num)
{
	if (num == -2147483648)
	{	
		write (1, "-2147483648", 11);
		return ;
	}
	if (num < 0)
	{
		write (1, "-", 1);
		num = num * -1;
	}
	if (num > 9)
	{	
		ft_putnbr (num / 10);
	}
	num = num % 10 + '0';
	write (1, &num, 1);
}
